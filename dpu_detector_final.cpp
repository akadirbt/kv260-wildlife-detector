/*
 * ============================================================
 *  Wildlife Detector — Kria KV260 FPGA
 *  Real-time animal detection using a YOLOv5 neural network
 *  accelerated by the on-chip DPU (Deep Learning Processing Unit).
 *
 *  How it works (big picture):
 *    1. Camera captures a frame via the MIPI/CSI interface.
 *    2. GStreamer reads the frame directly from the ISP pipeline
 *       and scales it down to 416×416 pixels — all in hardware,
 *       without touching the CPU.
 *    3. The frame is fed into the DPU, which runs the YOLOv5
 *       neural network on the FPGA fabric (~13 ms per frame).
 *    4. The output tensors are decoded into bounding boxes and
 *       class labels (bear, deer, squirrel, etc.).
 *    5. Non-Maximum Suppression (NMS) removes duplicate boxes.
 *    6. Results are printed to the terminal with FPS statistics.
 *
 *  Performance achieved:
 *    - ~67 FPS at 416×416 input resolution
 *    - Total latency: ~15 ms per frame (pre + DPU + post)
 *    - CPU is nearly idle — all heavy work runs on FPGA hardware
 *
 *  Compile (inside Docker container):
 *    g++ -O3 -std=c++17 -o dpu_detector dpu_detector.cpp \
 *        $(pkg-config --cflags --libs opencv4)             \
 *        $(pkg-config --cflags --libs gstreamer-1.0)       \
 *        -lvitis_ai_library-graph_runner                   \
 *        -lvart-runner -lxir -lglog -lpthread
 *
 *  Run:
 *    export XLNX_VART_FIRMWARE=/lib/firmware/xilinx/kv260-smartcam/kv260-smartcam.xclbin
 *    ./dpu_detector
 * ============================================================
 */

